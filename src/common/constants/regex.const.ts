/**
 * 正则表达式常量定义
 *
 * @description
 * 定义系统中常用的正则表达式，用于数据校验和格式验证。
 * 所有正则表达式均为只读常量，避免运行时创建新实例。
 */

/** 基础格式校验 */
export const FORMAT_REGEX = {
  /** 邮箱地址 - 支持常见邮箱格式 */
  EMAIL: /^[^\s@]+@[^\s@]+\.[^\s@]+$/,
  /** 中国大陆手机号 - 13-19 开头，11 位数字 */
  PHONE: /^1[3-9]\d{9}$/,
  /** 座机号码 - 支持区号+号码格式 */
  TELEPHONE: /^0\d{2,3}-?\d{7,8}$/,
  /** URL 地址 - 支持 http/https 协议 */
  URL: /^https?:\/\/[^\s]+$/,
  /** IPv4 地址 */
  IPV4: /^(\d{1,3}\.){3}\d{1,3}$/,
  /** IPv6 地址 */
  IPV6: /^([0-9a-fA-F]{1,4}:){7}[0-9a-fA-F]{1,4}$/,
  /** MAC 地址 */
  MAC: /^([0-9A-Fa-f]{2}[:-]){5}[0-9A-Fa-f]{2}$/,
  /** 邮政编码 - 中国大陆 6 位邮编 */
  POSTAL_CODE: /^\d{6}$/,
  /** 身份证号 - 18 位（含校验码） */
  ID_CARD:
    /^[1-9]\d{5}(18|19|20)\d{2}(0[1-9]|1[0-2])(0[1-9]|[12]\d|3[01])\d{3}[\dXx]$/,
  /** 统一社会信用代码 - 18 位 */
  CREDIT_CODE: /^[0-9A-HJ-NPQRTUWXY]{2}\d{6}[0-9A-HJ-NPQRTUWXY]{10}$/,
} as const;

/** 密码强度校验 */
export const PASSWORD_REGEX = {
  /** 弱密码 - 6-20 位，含字母和数字 */
  WEAK: /^(?=.*[A-Za-z])(?=.*\d)[A-Za-z\d@$!%*#?&]{6,20}$/,
  /** 中密码 - 8-20 位，含大小写字母和数字 */
  MEDIUM: /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)[A-Za-z\d@$!%*#?&]{8,20}$/,
  /** 强密码 - 8-20 位，含大小写字母、数字和特殊字符 */
  STRONG:
    /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*#?&])[A-Za-z\d@$!%*#?&]{8,20}$/,
} as const;

/** 内容格式校验 */
export const CONTENT_REGEX = {
  /** 用户名 - 2-20 位中英文、数字、下划线 */
  USERNAME: /^[a-zA-Z0-9_\u4e00-\u9fa5]{2,20}$/,
  /** 真实姓名 - 2-20 位中英文、点、空格 */
  REAL_NAME: /^[\u4e00-\u9fa5a-zA-Z·\s]{2,20}$/,
  /** 昵称 - 1-30 位任意字符 */
  NICKNAME: /^.{1,30}$/,
  /** 验证码 - 6 位数字 */
  VERIFY_CODE: /^\d{6}$/,
  /** 金额 - 支持两位小数 */
  AMOUNT: /^\d+(\.\d{1,2})?$/,
  /** 百分比 - 0-100，支持两位小数 */
  PERCENTAGE: /^(100(\.0{1,2})?|\d{1,2}(\.\d{1,2})?)%?$/,
  /** 百分比数值 - 0-100，支持两位小数（无百分号） */
  PERCENTAGE_VALUE: /^(100(\.0{1,2})?|\d{1,2}(\.\d{1,2})?)$/,
} as const;

/** 特殊格式校验 */
export const SPECIAL_REGEX = {
  /** UUID v4 */
  UUID: /^[0-9a-f]{8}-[0-9a-f]{4}-4[0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$/i,
  /** Base64 编码 */
  BASE64: /^(?:[A-Za-z0-9+/]{4})*(?:[A-Za-z0-9+/]{2}==|[A-Za-z0-9+/]{3}=)?$/,
  /** HTML 标签 - 检测是否包含 HTML */
  HTML: /<[^>]+>/,
  /** 纯数字 */
  NUMBER: /^\d+$/,
  /** 纯字母 */
  LETTER: /^[a-zA-Z]+$/,
  /** 字母数字组合 */
  ALPHANUMERIC: /^[a-zA-Z0-9]+$/,
  /** 不含特殊字符 */
  NO_SPECIAL_CHARS: /^[^\s@#$%^&*()+=\[\]{};:'",.<>?\\|`~!]+$/,
  /** 中文字符 */
  CHINESE: /[\u4e00-\u9fa5]/,
  /** 纯中文字符 */
  ONLY_CHINESE: /^[\u4e00-\u9fa5]+$/,
} as const;
